class MethodsInJava{
  public static void main (String []args){
    System.out.println("Hello World!!");
    greetUser();
    printFirstPattern();
  }

  public static void greetUser(){
    System.out.println("Good Morining Saurav");
  }

  public static  void printFirstPattern(){
    System.out.println("*");
    System.out.println("* *");
    System.out.println("* * *");
    System.out.println("* * * *");
    System.out.println("* * * * *");
    System.out.println("* * * * * *");
  }
}